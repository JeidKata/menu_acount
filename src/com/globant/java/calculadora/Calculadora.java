package com.globant.java.calculadora;

import java.util.Scanner;

public class Calculadora {

    public void sumar(byte n1, byte n2){
        System.out.println(n1+n2);
    }

    public void restar(byte n1, byte n2) {
        System.out.println(n1-n2);
    }

    public void multiplicar(byte n1, byte n2){
        System.out.println(n1*n2);
    }

    public void dividir(byte n1, byte n2){
        System.out.println(n1/n2);
    }
}
